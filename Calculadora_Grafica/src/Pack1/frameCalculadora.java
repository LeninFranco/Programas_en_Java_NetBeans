    private String operaciones(String signo, String valor1, String valor2){
	if(signo.equals("+")){
	    return String.valueOf(Double.parseDouble(valor1) + Double.parseDouble(valor2));
	}
	if(signo.equals("-")){
	    return String.valueOf(Double.parseDouble(valor1) - Double.parseDouble(valor2));
	}
	if(signo.equals("*")){
	    return String.valueOf(Double.parseDouble(valor1) * Double.parseDouble(valor2));
	}
	if(signo.equals("/")){
	    return String.valueOf(Double.parseDouble(valor1) / Double.parseDouble(valor2));
	}
	if(signo.equals("raiz")){
	    return String.valueOf(Math.sqrt(Double.parseDouble(valor1)));
	}
	if(signo.equals("x2")){
	    return String.valueOf(Math.pow(Double.parseDouble(valor1),2));
	}
	if(signo.equals("xy")){
	    return String.valueOf(Math.pow(Double.parseDouble(valor1),Double.parseDouble(valor2)));
	}
	if(signo.equals("1/x")){
	    return String.valueOf(1 / Double.parseDouble(valor1));
	}
	if(signo.equals("ex")){
	    return String.valueOf(Math.exp(Double.parseDouble(valor1)));
	}
	return "";
    }
    
