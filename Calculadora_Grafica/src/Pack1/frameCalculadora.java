    private void btnResultadoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnResultadoActionPerformed
        if(!txtPantalla.getText().equals("") && !signo.equals("")){
	    num2 = txtPantalla.getText();
	    txtPantalla.setText(operaciones(signo, num1, num2));
	    signo = "";
	    this.setTitle("Calculadora");
	}
    }//GEN-LAST:event_btnResultadoActionPerformed

    private void btnDelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDelActionPerformed
	if(txtPantalla.getText().length() > 0){
	    txtPantalla.setText(txtPantalla.getText().substring(0,txtPantalla.getText().length()-1));
	}
    }//GEN-LAST:event_btnDelActionPerformed

