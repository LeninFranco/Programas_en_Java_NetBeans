    public boolean emptyEntry(){
	return txtCodigo.getText().equals("") || txtFecha.getText().equals("") || txtID.getText().equals("") || txtCantidad.getText().equals("");
    }
    
    public void calcularTotal(){
	float sumatoria = 0;
	if(this.model.getRowCount() == 0){
	    lbTotal.setText("Total: $0.0");
	    return;
	}
	for (int i = model.getRowCount() -1 ; i >= 0 ; i--) {
            sumatoria += Float.parseFloat(String.valueOf(Table.getValueAt(i, 4)));
        }
	lbTotal.setText("Total: $" + sumatoria);
    }
    
    public void clearAll(){
	txtCantidad.setText("");
	txtCodigo.setText("");
	txtFecha.setText("");
	txtID.setText("");
	lbTotal.setText("Total $:");
	for (int i = model.getRowCount() -1 ; i >= 0 ; i--) {
            model.removeRow(i);
        }
    }
    
