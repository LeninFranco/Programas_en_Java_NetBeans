    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        if(!emptyEntry()){
	    int id = Integer.parseInt(txtID.getText());
	    if(operacionProducto.findOne(id) != null){
		Producto p = operacionProducto.findOne(id);
		int cant = Integer.parseInt(txtCantidad.getText());
		float subtotal = p.getPrecio() * cant;
		model.addRow(new Object[]{
		    p.getID(),
		    p.getNombre(),
		    p.getPrecio(),
		    cant,
		    subtotal
		});
		calcularTotal();
		txtID.setText("");
		txtCantidad.setText("");
	    }
	    else{
		JOptionPane.showMessageDialog(null,"No existe un producto con esa ID","ERROR",JOptionPane.ERROR_MESSAGE);
	    }
	}
	else{
	    JOptionPane.showMessageDialog(null,"Favor de llenar los campos","ERROR",JOptionPane.ERROR_MESSAGE);
	}
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        model.removeRow(Table.getSelectedRow());
	calcularTotal();
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnCompraActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCompraActionPerformed
        if(!emptyEntry()){
	    int codigo = Integer.parseInt(txtCodigo.getText());
	    if(operacionFactura.findOne(codigo) == null){
		Factura f = new Factura();
		f.setCodigo(codigo);
		f.setFecha(txtFecha.getText());
		f.setMetodoPago("Efectivo");
		operacionFactura.create(f);
		for (int i = this.model.getRowCount() -1 ; i >= 0 ; i--) {
		    int cant = Integer.parseInt(String.valueOf(Table.getValueAt(i,3)));
		    float subtotal = Float.parseFloat(String.valueOf(Table.getValueAt(i,4)));
		    int id = Integer.parseInt(String.valueOf(Table.getValueAt(i,0)));
		    operacionDetalle.create(cant, subtotal, codigo, id);
		}
		JOptionPane.showMessageDialog(null,"Se ha realizado la compra correctamente","Operacion Exitosa",JOptionPane.INFORMATION_MESSAGE);
		clearAll();
	    }
	    else{
		JOptionPane.showMessageDialog(null,"Ya existe una factura con ese codigo","ERROR",JOptionPane.ERROR_MESSAGE);
	    }
	}
	else{
	    JOptionPane.showMessageDialog(null,"Favor de llenar los campos","ERROR",JOptionPane.ERROR_MESSAGE);
	}
    }//GEN-LAST:event_btnCompraActionPerformed


