    private void fillTable(){
	for(Integer key : DataSource.keySet()){
	    Object[] filas = new Object[6];
	    filas[0] = DataSource.get(key).getId();
            filas[1] = DataSource.get(key).getNombre();
            filas[2] = DataSource.get(key).getAlbum();
            filas[3] = DataSource.get(key).getArtista();
            filas[4] = DataSource.get(key).getAnio();
            filas[5] = DataSource.get(key).getDuracion();
	    model.addRow(filas);
	}
    }
    
    private void clearTable(){
        for (int i = model.getRowCount() -1 ; i >= 0 ; i--) {
            model.removeRow(i);
        }
    }
    
    private void clearEntry(){
        txtID.setText("");
        txtCancion.setText("");
        txtAlbum.setText("");
        txtArtista.setText("");
        txtAnio.setText("");
        txtDuracion.setText("");
    }
    
    private boolean entryEmpty(){
        return txtID.getText().isEmpty() || txtCancion.getText().isEmpty() || txtAlbum.getText().isEmpty() || txtArtista.getText().isEmpty() || txtAnio.getText().isEmpty() || txtDuracion.getText().isEmpty();
    }
    
