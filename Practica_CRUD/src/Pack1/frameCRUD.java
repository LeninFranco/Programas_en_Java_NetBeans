    private void Table1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_Table1MouseClicked
        int Fila = Table1.getSelectedRow();
	Integer key = Integer.parseInt(Table1.getValueAt(Fila,0).toString());
	Cancion c = DataSource.get(key);
	txtID.setText(String.valueOf(c.getId()));
        txtCancion.setText(c.getNombre());
        txtAlbum.setText(c.getAlbum());
        txtArtista.setText(c.getArtista());
        txtAnio.setText(String.valueOf(c.getAnio()));
        txtDuracion.setText(c.getDuracion());
    }//GEN-LAST:event_Table1MouseClicked

    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        if(!entryEmpty()){
	    Integer key = Integer.parseInt(txtID.getText());
	    if(!DataSource.containsKey(key)){
		Cancion c = new Cancion();
		c.setId(Integer.parseInt(txtID.getText()));
		c.setNombre(txtCancion.getText());
		c.setArtista(txtArtista.getText());
		c.setAlbum(txtAlbum.getText());
		c.setAnio(Integer.parseInt(txtAnio.getText()));
		c.setDuracion(txtDuracion.getText());
		DataSource.put(c.getId(),c);
		clearTable();
		fillTable();
		JOptionPane.showMessageDialog(null,"El registro fue guardado correctamente","EXITO",JOptionPane.INFORMATION_MESSAGE);
	    }
	    else{
		JOptionPane.showMessageDialog(null,"El identificador ingresado ya existe","ERROR",JOptionPane.ERROR_MESSAGE);
	    }
	}
	else{
	    JOptionPane.showMessageDialog(null,"Favor de llenar todos los campos","ERROR",JOptionPane.ERROR_MESSAGE);
	}
	clearEntry();
    }//GEN-LAST:event_btnGuardarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        if(!txtID.getText().isEmpty()){
	    Integer key = Integer.parseInt(txtID.getText());
	    if(DataSource.containsKey(key)){
		DataSource.remove(key);
		clearTable();
		fillTable();
		JOptionPane.showMessageDialog(null,"El registro fue eliminado correctamente","EXITO",JOptionPane.INFORMATION_MESSAGE);
	    }
	    else{
		JOptionPane.showMessageDialog(null,"No existe el numero de cuenta ingresado en el registro","ERROR",JOptionPane.ERROR_MESSAGE);
	    }
	}
	else{
	    JOptionPane.showMessageDialog(null,"Favor de llenar el campo ID","ERROR",JOptionPane.ERROR_MESSAGE);
	}
	clearEntry();
    }//GEN-LAST:event_btnEliminarActionPerformed

